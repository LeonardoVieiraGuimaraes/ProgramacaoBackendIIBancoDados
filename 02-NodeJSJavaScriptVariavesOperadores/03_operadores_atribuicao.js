let x = 5; // Atribuição: x é agora 5
console.log(`Atribuição: x é agora ${x}`); // Saída: Atribuição: x é agora 5

x += 3; // Atribuição de adição: x é agora 8
console.log(`Atribuição de adição: x é agora ${x}`); // Saída: Atribuição de adição: x é agora 8

x -= 2; // Atribuição de subtração: x é agora 6
console.log(`Atribuição de subtração: x é agora ${x}`); // Saída: Atribuição de subtração: x é agora 6

x *= 2; // Atribuição de multiplicação: x é agora 12
console.log(`Atribuição de multiplicação: x é agora ${x}`); // Saída: Atribuição de multiplicação: x é agora 12

x /= 4; // Atribuição de divisão: x é agora 3
console.log(`Atribuição de divisão: x é agora ${x}`); // Saída: Atribuição de divisão: x é agora 3

x %= 2; // Atribuição de módulo: x é agora 1
console.log(`Atribuição de módulo: x é agora ${x}`); // Saída: Atribuição de módulo: x é agora 1

x **= 2; // Atribuição de exponenciação: x é agora 1
console.log(`Atribuição de exponenciação: x é agora ${x}`); // Saída: Atribuição de exponenciação: x é agora 1

x <<= 2; // Atribuição de deslocamento à esquerda: x é agora 4
console.log(`Atribuição de deslocamento à esquerda: x é agora ${x}`); // Saída: Atribuição de deslocamento à esquerda: x é agora 4

x >>= 1; // Atribuição de deslocamento à direita: x é agora 2
console.log(`Atribuição de deslocamento à direita: x é agora ${x}`); // Saída: Atribuição de deslocamento à direita: x é agora 2

x >>>= 1; // Atribuição de deslocamento à direita sem sinal: x é agora 1
console.log(`Atribuição de deslocamento à direita sem sinal: x é agora ${x}`); // Saída: Atribuição de deslocamento à direita sem sinal: x é agora 1
