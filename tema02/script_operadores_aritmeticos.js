// Operador de Aritméticos:

let a = 5,
  b = 10;

// Adição
console.log(a + b); // Saída: 15

// Subtração
console.log(a - b); // Saída: -5

// Multiplicação
console.log(a * b); // Saída: 50

// Divisão
console.log(a / b); // Saída: 0.5

// Módulo (resto da divisão)
console.log(a % b); // Saída: 5

// Incremento
a++;
console.log(a); // Saída: 6

// Decremento
b--;
console.log(b); // Saída: 9
