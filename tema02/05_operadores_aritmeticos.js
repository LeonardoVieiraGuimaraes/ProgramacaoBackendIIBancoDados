let a = 5,
  b = 10;

// Adição
console.log(`Adição: a + b = ${a + b}`); // Saída: Adição: a + b = 15

// Subtração
console.log(`Subtração: a - b = ${a - b}`); // Saída: Subtração: a - b = -5

// Multiplicação
console.log(`Multiplicação: a * b = ${a * b}`); // Saída: Multiplicação: a * b = 50

// Divisão
console.log(`Divisão: a / b = ${a / b}`); // Saída: Divisão: a / b = 0.5

// Módulo (resto da divisão)
console.log(`Módulo: a % b = ${a % b}`); // Saída: Módulo: a % b = 5

// Incremento
console.log(`Incremento: a++ = ${++a}`); // Saída: Incremento: a++ = 6

// Decremento
console.log(`Decremento: b-- = ${--b}`); // Saída: Decremento: b-- = 9
