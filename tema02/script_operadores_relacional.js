// Exemplo de operador de Relacional:

let a = 5,
  b = 10,
  c = "5";

// Igual a
console.log(a == c); // Saída: true

// Diferente de
console.log(a != b); // Saída: true

// Estritamente igual a
console.log(a === c); // Saída: false

// Estritamente diferente de
console.log(a !== c); // Saída: true

// Menor que
console.log(a < b); // Saída: true

// Maior que
console.log(b > a); // Saída: true

// Menor ou igual a
console.log(a <= b); // Saída: true

// Maior ou igual a
console.log(b >= a); // Saída: true
